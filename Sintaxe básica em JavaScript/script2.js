// adicão
var adicao = 10 + 10
console.log(adicao)

// subtração
var subtracao = 10 - 10
console.log(subtracao)

// multiplicação
var multiplicacao = 10 * 10
console.log(multiplicacao)

// divisão
var divisao = 10 / 10
console.log(divisao)

// divisão real
var divisaoReal = 10 / 2
console.log(divisaoReal)

// divisao inteira
var divisaoInteira = 10 / 2
console.log(divisaoInteira)

// potenciação
var potencia = 10 ** 2
console.log(potencia)

// maior que
var maiorQue = 10 > 10
console.log(maiorQue) // false

// menor que
var menorQue = 10 < 10
console.log(menorQue) // false

// maior ou igual
var maiorOuIgual = 10 >= 10
console.log(maiorOuIgual) // true

// menor ou igual
var menorOuIgual = 10 <= 10
console.log(menorOuIgual) // true

// Operador lógico e
var e = 10 > 10 && 10 > 5
console.log(e) // false

// operador lógico ou
var ou = 10 > 10 || 10 > 5
console.log(ou) // true

// operador lógico negação
var negacao = !(20 > 10)
console.log(negacao) // false